$(document).ready(function(){
 
  $('#onView').css('opacity', '0');
 
  $('#onView').waypoint(function() {
      $('#onView').addClass('bounceInLeft');
      $('#onView').css('opacity', '100');
  }, { offset: '80%' });
 
});

