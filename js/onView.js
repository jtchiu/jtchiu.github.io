// slideInLeft
$(document).ready(function(){
 
  $('#onView').css('opacity', '0');
 
  $('#onView').waypoint(function() {
      $('#onView').addClass('slideInLeft');
      $('#onView').css('opacity', '100');
  }, { offset: '80%' });
 
});

